// Get related articles for a given article
export function getRelatedArticles(canonical, petType, count = 6) {
  const related = [];
  const parts = canonical.split("/").filter(Boolean);
  const category = parts[0]; // can-dogs-eat, can-cats-eat, pet-safety, brand-review
  const currentSlug = parts[1];

  if (category === "can-dogs-eat") {
    // Mix: some same-safety, some different, plus relevant safety articles
    const others = DOG_FOODS.filter(a => a.slug !== currentSlug);
    const currentItem = DOG_FOODS.find(a => a.slug === currentSlug);
    const currentSafety = currentItem?.safety;

    // Add 1-2 from same safety level
    const sameSafety = others.filter(a => a.safety === currentSafety).sort(() => 0.5 - Math.random());
    related.push(...sameSafety.slice(0, 2).map(a => ({ ...a, href: `/can-dogs-eat/${a.slug}` })));

    // Add 1-2 from different safety
    const diffSafety = others.filter(a => a.safety !== currentSafety).sort(() => 0.5 - Math.random());
    related.push(...diffSafety.slice(0, 2).map(a => ({ ...a, href: `/can-dogs-eat/${a.slug}` })));

    // Add relevant safety articles
    const safetyLinks = PET_SAFETY.filter(a => a.petType === "dog").slice(0, 2);
    related.push(...safetyLinks.map(a => ({ ...a, href: `/pet-safety/${a.slug}` })));
  } else if (category === "can-cats-eat") {
    const others = CAT_FOODS.filter(a => a.slug !== currentSlug);
    const currentItem = CAT_FOODS.find(a => a.slug === currentSlug);
    const currentSafety = currentItem?.safety;

    const sameSafety = others.filter(a => a.safety === currentSafety).sort(() => 0.5 - Math.random());
    related.push(...sameSafety.slice(0, 2).map(a => ({ ...a, href: `/can-cats-eat/${a.slug}` })));

    const diffSafety = others.filter(a => a.safety !== currentSafety).sort(() => 0.5 - Math.random());
    related.push(...diffSafety.slice(0, 2).map(a => ({ ...a, href: `/can-cats-eat/${a.slug}` })));

    const safetyLinks = PET_SAFETY.filter(a => a.petType === "cat").slice(0, 2);
    related.push(...safetyLinks.map(a => ({ ...a, href: `/pet-safety/${a.slug}` })));
  } else if (category === "pet-safety") {
    // Link to food articles and other safety articles
    const currentItem = PET_SAFETY.find(a => a.slug === currentSlug);
    const pet = currentItem?.petType || "dog";
    const foodList = pet === "dog" ? DOG_FOODS : CAT_FOODS;
    const foodCategory = pet === "dog" ? "can-dogs-eat" : "can-cats-eat";

    // Add some toxic foods
    const toxicFoods = foodList.filter(a => a.safety === "toxic").slice(0, 2);
    related.push(...toxicFoods.map(a => ({ ...a, href: `/${foodCategory}/${a.slug}` })));

    // Add some safe foods
    const safeFoods = foodList.filter(a => a.safety === "safe").sort(() => 0.5 - Math.random()).slice(0, 2);
    related.push(...safeFoods.map(a => ({ ...a, href: `/${foodCategory}/${a.slug}` })));

    // Other safety articles
    const otherSafety = PET_SAFETY.filter(a => a.slug !== currentSlug && a.petType === pet).slice(0, 2);
    related.push(...otherSafety.map(a => ({ ...a, href: `/pet-safety/${a.slug}` })));
  } else if (category === "brand-review") {
    // Other brand reviews
    const others = BRAND_REVIEWS.filter(a => a.slug !== currentSlug);
    related.push(...others.slice(0, 4).map(a => ({ ...a, href: `/brand-review/${a.slug}` })));

    // Add popular food articles
    related.push({ slug: "chicken", food: "Chicken", safety: "safe", href: "/can-dogs-eat/chicken" });
    related.push({ slug: "salmon", food: "Salmon", safety: "safe", href: "/can-dogs-eat/salmon" });
  }

  return related.slice(0, count);
}
